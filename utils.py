def sparse_to_dense_torch(locs, values, dim, default_val, device, dtype=torch.float):
    dense = torch.full([dim[0], dim[1], dim[2]], float(default_val), device=device, dtype=dtype)
    if locs.shape[0] > 0:
        dense[locs[:, 0], locs[:, 1], locs[:, 2]] = values
    return dense


